// 業務項目の総数を計算する関数
export const getTotalBusinessItems = () => {
  let total = 0;
  Object.values(DETAILED_BUSINESS_ITEMS).forEach(category => {
    Object.values(category).forEach(phase => {
      total += phase.length;
    });
  });
  return total;
};

// 特定のカテゴリとフェーズの業務項目を取得する関数
export const getBusinessItems = (category, phase) => {
  return DETAILED_BUSINESS_ITEMS[category]?.[phase] || [];
};

// 全ての業務項目をフラットな配列で取得する関数
export const getAllBusinessItemsFlat = () => {
  const items = [];
  Object.entries(DETAILED_BUSINESS_ITEMS).forEach(([category, phases]) => {
    Object.entries(phases).forEach(([phase, items_list]) => {
      items_list.forEach(item => {
        items.push({
          category,
          phase,
          item
        });
      });
    });
  });
  return items;
};